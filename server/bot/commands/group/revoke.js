export default {
    name: 'revoke',
    alias: ['revokelink', 'resetlink'],
    category: 'group',
    description: 'Revoke group invite link',
    ownerOnly: false,

    async execute(sock, msg, args, PREFIX, extra) {
        const jid = msg.key.remoteJid;
        if (!jid.endsWith('@g.us')) {
            return sock.sendMessage(jid, { text: 'в”Ңв”Җв§ӯ GROUP ONLY в§ӯв”Җв”җ\nв”ңв—Ҷ This command works in groups only.\nв””в”Җв§ӯв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв§ӯв”Җв”ҳ' }, { quoted: msg });
        }

        const metadata = await sock.groupMetadata(jid).catch(() => null);
        const participant = msg.key.participant || msg.key.remoteJid;
        const isAdmin = metadata?.participants?.find(p => p.id === participant)?.admin;

        if (!isAdmin) {
            return sock.sendMessage(jid, { text: 'в”Ңв”Җв§ӯ ADMIN ONLY в§ӯв”Җв”җ\nв”ңв—Ҷ Only group admins can revoke the invite link.\nв””в”Җв§ӯв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв§ӯв”Җв”ҳ' }, { quoted: msg });
        }

        try {
            await sock.groupRevokeInvite(jid);

            let text = 'в”Ңв”Җв§ӯ LINK REVOKED в§ӯв”Җв”җ\n';
            text += 'в”ңв—Ҷ The old invite link has been revoked.\n';
            text += 'в”ңв—Ҷ A new link has been generated.\n';
            text += '\n';
            text += `в”ңв—Ҷ Use ${PREFIX}grouplink to get the new link.\n`;
            text += 'в””в”Җв§ӯв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв§ӯв”Җв”ҳ';

            await sock.sendMessage(jid, { text }, { quoted: msg });
        } catch (error) {
            await sock.sendMessage(jid, { text: 'в”Ңв”Җв§ӯ ERROR в§ӯв”Җв”җ\nв”ңв—Ҷ Failed to revoke invite link.\nв”ңв—Ҷ Make sure the bot is an admin.\nв””в”Җв§ӯв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв§ӯв”Җв”ҳ' }, { quoted: msg });
        }
    }
};
